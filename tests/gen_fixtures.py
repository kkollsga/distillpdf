def gen_lists():
    pdf = os.path.join(OUT, "lists.pdf")
    c = canvas.Canvas(pdf, pagesize=letter)
    title(c, "List Showcase")
    y = PAGE_H - 120
    wrapped = ("Star-marked delta wraps onto a second line so its continuation is folded "
               "back into the same list item rather than splitting off as a paragraph.")
    star = ["Star-marked alpha is the first unordered item.",
            "Star-marked beta is the second unordered item.",
            "Star-marked gamma is the third unordered item.",
            wrapped]
    numbered = ["First ordered step in the procedure.",
                "Second ordered step in the procedure.",
                "Third ordered step in the procedure."]
    colon = ["Question one introduced after a colon line.",
             "Question two introduced after a colon line."]
    # '*' is a recognised weak bullet; reportlab cannot emit a real U+2022 bullet in
    # Helvetica (it maps to 0x7f), so the unordered list uses '*'.
    y = para(c, "An unordered list, including one wrapped item:", y, gap=4)
    for b in star:
        y = para(c, "*  " + b, y, indent=18, gap=3)
    y -= 6
    y = para(c, "An ordered numbered list:", y, gap=4)
    for i, n in enumerate(numbered, 1):
        y = para(c, f"{i}. {n}", y, indent=18, gap=3)
    y -= 6
    # colon-introduced: a ':' line immediately precedes numbered items carrying labels.
    y = para(c, "The study asks the following questions:", y, gap=4)
    for i, q in enumerate(colon, 1):
        y = para(c, f"{i}. {q}", y, indent=18, gap=3)
    c.showPage()
    c.save()
    GT["lists.pdf"] = {
        "numbered": numbered, "colon": colon,
        "wrapped_snippet": "folded back into the same list item",
        "unordered_items": star, "ordered_items": numbered + colon,
    }


