OUT = os.path.join(os.path.dirname(__file__), "fixtures_pdf")
os.makedirs(OUT, exist_ok=True)
PAGE_W, PAGE_H = letter
LM = 72
COL_W = PAGE_W - 2 * LM
BODY_F, BODY_S, LEAD = "Helvetica", 10.5, 14
GT = {}


def title(c, text):
    c.setFont("Helvetica-Bold", 19)
    c.drawCentredString(PAGE_W / 2, PAGE_H - 80, text)


def para(c, text, y, indent=0, gap=8, font=BODY_F, size=BODY_S, width=None):
    c.setFont(font, size)
    w = (width if width is not None else COL_W) - indent
    for line in simpleSplit(text, font, size, w):
        c.drawString(LM + indent, y, line)
        y -= LEAD
    return y - gap


def heading(c, text, y, size=13, gap=6):
    c.setFont("Helvetica-Bold", size)
    c.drawString(LM, y, text)
    return y - (size + gap)


