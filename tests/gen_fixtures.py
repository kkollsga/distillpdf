def gen_figures():
    """Page 1: a raster image + caption + an inline cross-reference. Page 2: a vector
    chart (>=6 paths) with axis labels and a 90deg-rotated y-axis title + caption. One
    figure per page so each caption has an unambiguous nearest figure."""
    pdf = os.path.join(OUT, "figures.pdf")
    png = os.path.join(OUT, "_bar.png")
    _bar_png(png)
    c = canvas.Canvas(pdf, pagesize=letter)
    title(c, "Figure Showcase")
    y = PAGE_H - 120
    y = para(c, "This document contains a raster figure and a vector figure. As shown in "
                "Figure 1 the bars differ in height, which the analysis discusses below.", y)
    img_w, img_h = 230, 146
    c.drawImage(png, LM, y - img_h, width=img_w, height=img_h)
    y -= img_h + 14
    c.setFont("Helvetica", 9.5)
    c.drawString(LM, y, "Figure 1: A raster bar chart comparing two measured groups.")
    c.showPage()
    # page 2 — vector figure
    y = PAGE_H - 120
    ox, oy, vw, vh = LM + 50, y - 150, 220, 130
    c.setLineWidth(1)
    c.line(ox, oy, ox + vw, oy)
    c.line(ox, oy, ox, oy + vh)
    for i in range(1, 4):
        gy = oy + vh * i / 4
        c.line(ox, gy, ox + vw, gy)
    c.setFillColorRGB(0.25, 0.45, 0.8)
    for i, bh in enumerate((40, 95, 65, 110)):
        c.rect(ox + 20 + i * 48, oy, 28, bh, fill=1, stroke=1)
    c.setFillColorRGB(0, 0, 0)
    c.setFont("Helvetica", 8)
    for i, lab in enumerate(("Q1", "Q2", "Q3", "Q4")):
        c.drawString(ox + 28 + i * 48, oy - 11, lab)
    c.saveState()
    c.translate(ox - 16, oy + vh / 2 - 20)
    c.rotate(90)
    c.setFont("Helvetica", 8)
    c.drawString(0, 0, "Amplitude")
    c.restoreState()
    yc = oy - 24
    c.setFont("Helvetica", 9.5)
    c.drawString(LM, yc, "Figure 2: A vector line chart with a rotated axis title.")
    c.showPage()
    c.save()
    os.remove(png)
    GT["figures.pdf"] = {
        "fig1_caption": "Figure 1: A raster bar chart comparing two measured groups.",
        "fig2_caption": "Figure 2: A vector line chart with a rotated axis title.",
        "rotated_label": "Amplitude",
        "inline_xref": "As shown in Figure 1 the bars differ",
        "n_figures": 2,
    }


def _vector_chart(c, ox, oy, vw, vh):
    c.setLineWidth(1)
    c.line(ox, oy, ox + vw, oy)
    c.line(ox, oy, ox, oy + vh)
    for i in range(1, 4):
        c.line(ox, oy + vh * i / 4, ox + vw, oy + vh * i / 4)
    c.setFillColorRGB(0.25, 0.45, 0.8)
    for i, bh in enumerate((40, 95, 65, 110)):
        c.rect(ox + 20 + i * 48, oy, 28, min(bh, vh - 4), fill=1, stroke=1)
    c.setFillColorRGB(0, 0, 0)


