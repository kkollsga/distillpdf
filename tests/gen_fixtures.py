def _bar_png(path):
    img = Image.new("RGB", (300, 190), "white")
    d = ImageDraw.Draw(img)
    d.rectangle([8, 8, 291, 181], outline="black", width=2)
    d.rectangle([50, 110, 110, 175], fill=(60, 110, 200))
    d.rectangle([170, 60, 230, 175], fill=(200, 90, 70))
