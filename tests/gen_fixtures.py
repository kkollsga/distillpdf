#!/usr/bin/env python3
"""Generate the OWNED, corpus-free fixture PDFs that the CI suite asserts against.

Every PDF here is generated by us (no third-party content), committed, and paired with
ground truth in ``fixtures_pdf/groundtruth.json``. Together with demo.pdf (gen_demo.py)
and the synthetic table corpus (gen_tables.py) these reproduce — on data we can share
freely — the coverage the gitignored corpus tests give locally: headings, lists,
figures (raster + vector), links/anchors, two-column reading order, typography
(sub/superscript, accents, de-hyphenation), math-vs-table/heading discrimination, and
word-level span granularity.

Low-level canvas (absolute text positioning) is used so the layout matches how text is
really placed and extraction is deterministic; the two-column fixture uses platypus
frames (per-block ``cm``) so it also exercises the CTM path; the links fixture is a
hand-written PDF so it carries a real ``/Names /Dests`` name tree (named-destination
anchors, which reportlab does not emit).

    python tests/gen_fixtures.py        # needs reportlab + pillow (dev only)
"""
import json
import os

from PIL import Image, ImageDraw
from reportlab.lib.pagesizes import letter
from reportlab.lib.utils import simpleSplit
from reportlab.pdfgen import canvas
from reportlab.platypus import BaseDocTemplate, Frame, PageTemplate, Paragraph
